<section>
	<h2>Om oss</h2>

	<p>Sunnhordland Interkommunale Miljøverk IKS (SIM) er ei moderne avfalls- og attvinningsverksemd. Selskapet vart stifta i mai 1990 og er eigd av kommunane Austevoll, Bømlo, Fitjar, Kvinnherad, Stord, Sveio og Tysnes. Administrasjonen ligg i Svartasmoget i Fitjar kommune.</p>

	<p>SIM har ansvar for innsamling av alt hushaldsavfall og slam frå dei 60 076 innbyggjarane og 7 572 hyttekundane i medlemskommunane. I alle kommunane er det kjeldesortering med dunkar for høvesvis bio-, papir- og restavfall og betjente miljøsentralar.</p>

	<p>I Svartasmoget er det administrasjonsbygg, deponi, miljøsentral, hall og utstyr for kompostering av våtorganisk avfall, i tillegg til anlegg for mottak og handsaming av avløpsslam. På Heiane, i Stord kommune, har selskapet omlastehall. Det er i tillegg miljøsentralar i alle eigarkommunane. SIM legg vekt på førebyggjande tiltak for å redusera miljøforureining av luft, vatn og nærområde. Selskapet arbeider for å oppfylla miljøkrav og å utnytta ressursane i avfallet.</p>
</section>

<section>
	<h3>&sect; 4 Føremål</h3>

	<ol>
		<li>Innsamling og transport av alle avfallstypar etter nærare avtale med kommunane</li>
		<li>Bygging og drift av behandlingsanlegg for avfall.</li>
		<li>Selskapet skal der dette er naturleg, søkja å etablera samarbeid med andre kommunar, regionar og selskap med tanke på å få til rasjonelle og miljørettaløysingar når det gjeld gjenvinning.</li>
		<li>Selskapet har ikkje erverv til føremål.</li>
	</ol>

	<h3>&sect; 5 Selskapet sine oppgåver:</h3>

	<ol>
		<li>Selskapet skal byggja og driva innsamlingssystem, mottak, gjenvinningsanlegg, mellomlagring og vidaresending av spesialavfall og avfall som kan gjenvinnast, resirkulerast eller må sendast vidare for destruksjon eller gjenvinning.</li>
		<li>Selskapet skal leggja særleg vekt på miljøvenleg drift av alle ledd i avfallshandsaminga.</li>
		<li>Selskapet skal i nært samarbeid med kommunane informera publikum og næringsdrivande om drifta i selskapet.</li>
		<li>Selskapet skal i informasjonsarbeidet leggja vekt på å gje auka kunnskap og forståing for miljøvenlege avfallshandsamingsmetodar.</li>
	</ol>
</section>

<section>
	<h3>Organisering</h3>

	<p>Representantskapet er det øvste organet i SIM. Eigarkommunane vel medlemer til representantskapet, og representantskapet vel styret. Dagleg leiar, administrasjonen og driftspersonellet står for den daglege drifta.</p>

	<h3>Representantskap</h3>

	<p>Folketalet avgjer kor mange medlemer kvar eigarkommune har i representantskapet. Kommunestyra vel medlemer og personlege varamedlemer til representantskapet for fire år om gongen, i samsvar med den kommunale valbolken. Dei 24 medlemene er fordelte slik: Austevoll 2, Bømlo 4, Fitjar 2, Kvinnherad 5, Stord 7, Sveio 2 og Tysnes 2. Representantskapet hadde eitt møte i 2014 (26. april) og handsama 13 saker.</p>

	<h3>Styre</h3>

	<p>Det er éin styremedlem frå kvar kommune, med personleg varamedlem. Styremedlemene vert valde for to år om gongen, og slik at halvparten står på val kvart år. I tillegg er det éin representant frå dei tilsette i styret. Representantskapet vel styremedlemene etter at medlemskommunane har peika ut kandidatar. Styret hadde elleve møte i 2014.</p>
</section>

<section>
	<h2>Dotterselskap og andre eigarinteresser</h2>

	<h3>SIM Næring AS  (heileigd)</h3>

	<p>Målsetjinga til selskapet var å gje næringskundar eit heilskapleg og god avfallshandtering. Kontor og sorteringsanlegg var på Eldøyane næringspark på Eldøyane i Stord kommune. 31.12.13 hadde selskapet 57 tilsette. Selskapet vart seld til Ragn- Sells AS 1. oktober.</p>

	<h3>SIM Tankrenovasjon AS (heileigd av SIM N)</h3>

	<p>SIM Tank heldt til på Frakkagjerd i Nord-Rogaland og tilbydde slamtømming, innsamling av farleg avfall og suge- og spyletenester. 31.12.13 hadde selskapet 19 tilsette. Selskapet vart seld til Ragn- Sells AS 1. oktober.</p>

	<h3>SLab</h3>

	<p>SLab er eit laboratorium for akvakultur, vatn og mat og vart stifta januar 2004. SIM har ein eigardel på 7,4 %.</p>

	<h3>Rekom AS</h3>

	<p>Rekom er eigd av 58 kommunar og interkommunale renovasjonsselskap, selskapet skal omsetja avfallsfraksjonar. SIM eig 200 av 4 699 aksjar i Rekom AS, ein eigardel på 4,26 %.</p>
</section>