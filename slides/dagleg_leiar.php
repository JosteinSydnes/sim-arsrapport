<section>
	<p>Avfallsbransjen har gått gjennom store endringar dei siste tiåra. Det er også ein medverkande årsak til at ein meinte det var rett å selje SIM sine dotterselskap, SIM Næring AS og SIM Tankrenovasjon AS i 2014. SIM Næring AS har vore eigd av SIM sidan 1997 og vart oppretta for å  sikra at næringslivet hadde eit tilbod for sitt avfall. Marknaden er ikkje lenger slik at det er mangel på aktørar og for at dei to selskapa skulle kunne utvikle seg vidare  var det rett å selja. For SIM sine abonnentar vil det ikkje medføra merkbare endringar. Selskapet vil framleis ha fokus på å gje innbyggjarane best mogeleg teneste til rett pris.</p>

	<p>Selskapet sine miljøsentralar er eit viktig bidrag for å leggje til rette for at innbyggjarane kan opptre miljørett. Og veksten i besøkstalet viser at miljøsentralen er eit svært populært tilbod. Dersom trenden dei siste åra held seg vil besøkstalet doble seg i løpet av dei neste fire åra, det vil tilseie eit besøkstal på over 120 000. Dette er ein auke SIM er budd på og vil vera med og leggja til rette for.</p>
</section>

<section>
	<p>Driftsmessig går selskapet godt. Ein registrerer få klager og kundeundersøkingar viser at innbyggjarane er nøgde med tenestene. Selskapet må likevel ha fokus på utvikling og forbetring. Nye krav og nye ordningar fører til at ein lyt tilpassa drifta og ordningane deretter. Selskapet er, som eit av to i landet, i gong med henting av tekstilar. Eit tilbod som foreløpig har vore ei prøveordning i ein kommune, men som har gjeve gode erfaringar og resultat. Drift av deponiet har endra seg mykje sidan selskapet vart oppretta. Frå å vera hovudaktivitet til å vera ein aktivitet ein vurderer  å avslutta. Foreløpig har ein fokus på drift også dei næraste åra.</p>

	<p>Selskapet har nedfelt i sine vedtekter at ein skal ha fokus på gode og miljørette løysingar for innbyggjarane i Sunnhordland. Eit mål er å auka graden av avfall som går til materialgjenvinning og ombruk. Eit anna mål er å gje innbyggjarane informasjon om miljørette handlingar, gjennom haldningsskapande arbeid og ved å leggje til rette for at ein kan opptre miljørett. Selskapet arbeider også for at  næringslivet og eigarkommunane skal ha miljørette løysingar som er framtidsretta.</p>
</section>

<section>
	<p>SIM har dyktige medarbeidarar som har fokus på kundetilfredshet og gode løysingar. Selskapet og dei tilsette er rusta til å møta nye utfordringar i ein bransje som stadig er i endring.
Takk til alle medlemskommunar, kundar, samarbeidspartnarar og tilsette for godt samarbeid i 2014.</p>

