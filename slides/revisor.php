<section>
	<h3>Fråsegn om årsrekneskapen</h3>

	<p>Vi har revidert årsrekneskapen for Sunnhordland Interkommunale Miljøverk IKS som inneheld selskapsrekneskap, som syner eit overskot på kr 36 655 222 , og konsernrekneskap , som syner eit overskot på kr 34 887 746 . Selskapsrekneskapen og konsernrekneskapen er samansett av balanse per 31 . desember 2014 , resultatrekneskap og kontantstraumoppstilling for rekneskapsåret avslutta per denne datoen, pliktige budsjettopplysningar og ei skildring av vesentlege rekneskapsprinsipp som er nytta og andre noteopplysningar.</p>
</section>

<section>
	<h3>Styret og dagleg leiars ansvar for årsrekneskapen</h3>
	<p>Styret og dagleg leiar er ansvarleg for å utarbeida årsrekneskapen og for at den gir eit rettvisande bilete i samsvar med rekneskapslovas reglar og god rekneskapsskikk i Noreg, tilleggsreglar i lov om interkommunale selskap, forskrift om årsbudsjett, årsrekneskap og årsmelding for interkommunale selskap, og for slik intern kontroll som styret og dagleg leiar finn nødvendig for å gjera det mogleg a utarbeida ein årsrekneskap som ikkje inneheld vesentleg feilinformasjon, korkje som følgje av misleg framferd eller feil.</p>
</section>

<section>
	<h3>Revisors oppgåver og plikter</h3>

	<p>Oppgåva vår er, på grunnlag av revisjonen vår, å gi uttrykk for ei meining om denne årsrekneskapen. Vi har gjennomført revisjonen i samsvar med lov, forskrift og god revisjonsskikk i Noreg, medrekna International Standards on Auditing. Revisjonsstandardane krev at vi etterlever etiske krav og planlegg og gjennomfører revisjonen for å oppnå tryggande sikkerheit for at årsrekneskapen ikkje inneheld vesentleg feilinformasjon.</p>

	<p>Ein revisjon inneber utføring av handlingar for å innhente revisjonsbevis for beløpa og opplysningane i årsrekneskapen. Dei valde handlingane avheng av revisors skjønn, mellom anna vurderinga av risikoane for at årsrekneskapen inneheld vesentleg feilinformasjon, anten det skuldast misleg framferd eller feil. Ved ei slik risikovurdering tar revisor omsyn til den interne kontrollen som er relevant for si utarbeiding av ein årsrekneskap som gir eit rettvisande bilete. Føremålet er å utforme revisjonshandlingar som er formålstenlege etter tilhøva, men ikkje å gi uttrykk for ei meining om effektiviteten av sin interne kontroll. Ein revisjon omfattar også ei vurdering av om dei rekneskapsprinsippa som er nytta, er formålstenlege, og om rekneskapsestimata som er utarbeidde av leiinga, er rimelege, samt ei vurdering av den samla presentasjonen av årsrekneskapen. Vi meiner at innhenta revisjonsbevis er tilstrekkeleg og formålstenleg som grunnlag for konklusjonen vår.</p>
</section>

<section>
	<h3>Konklusjon</h3>

	<p>Etter vår meining er årsrekneskapen avgjeven i samsvar med lov og forskriftar og gir eit rettvisande bilete av den finansielle stillinga til konsernet Sunnhordland Interkommunale Miljøverk IKS per 31. desember 2014 og av resultata for og kontantstraumar for rekneskapsåret som blei avslutta per denne datoen i samsvar med rekneskapslov as reglar og god rekneskapsskikk i Noreg.</p>
</section>

<section>
	<h3>Uttale om andre tilhøve</h3>

