					<section>
						<h3>Driftsaktivitet</h3>
						<p>Sunnhordland Interkommunale Miljøverk IKS (SIM) sin hovudaktivitet er innsamling og handsaming av kommunalt avfall, tømming av slamtankar og handsaming av avvatna slam. I tillegg tek selskapet imot avfall frå næringslivet til slutthandsaming ved komposteringsanlegg eller deponi. Selskapet sitt hovudkontor ligg i Fitjar kommune, aktiviteten går føre seg i kommunane Austevoll, Bømlo, Fitjar, Kvinnherad, Stord, Sveio og Tysnes. Kundane er godt nøgde med selskapet og renovasjonsordninga, dersom ein samanliknar talet på klager sett opp mot talet på kundar. Selskapet har ingen pågåande forsknings- eller utviklingsaktivitetar.
						</p>
					</section>

					<section>
						<h3>Økonomi</h3>
						<p>I 2014 hadde SIM ei inntekt på 81 millionar kroner. Driftsresultatet til selskapet var på minus 14,4 millionar kroner. Salet av dotterselskapa gav ein gevinst på 49,7 milllionar kroner, slik at resultatet til selskapet er eit overskot på37,7 millionar kroner. Gevinsten frå salet av dotterselskapa vert sett av i eit miljøfond, i tråd med vedtak i representantskapet.</p>

						<p>SIM har ansvar for å avslutta deponiet på ein tilfredsstillande måte, og for å driva det forsvarleg i minst 30 år etter avslutting. Selskapet skal årleg vurdera kva som er nødvendige avsetjingar for å dekke framtidige kostnader, i rekneskapen for 2014 er det sett av 16,1 millionar kroner ekstra slik at det totalt er sett av 66 millionar kroner i rekneskapen.</p>

						<p>Selskapet hadde eit underskot på kommunalt avfall på 0,46 millionar kroner, som vart trekt av renovasjonsfondet, Dette er ved slutten av året 10,8 millionar. Slamtømeaktivitetane hadde eit overskot på 0,78 millionar kroner. Overskotet vert tilført slamfondet som ved årsskiftet var på pluss 2,3 millionar kroner. Fonda skal byggjast ned dei næraste åra. Ved berekning av sjølvkost (etterkalkyle) legg selskapet til grunn lova, selskapsavtalen og rettleiaren for berekning av sjølvkost. Selskapet belastar ikkje gebyra kalkulatoriske renter.</p>

						<p>Styret meiner SIM er godt budd på framtidige miljøkrav, då eigenkapitalen i selskapet er god. Årsrekneskapen og konsernrekneskapen gir eit rett bilete av SIM og konsernet sine eigneluter og gjeld, finansiell stilling og resultat. Føresetnadene for framleis drift er til stades, og styret stadfestar desse.</p>
					</section>

					<section>
						<h3>Finansiell risiko</h3>

						<p>SIM sin strategi er å investera med låg til moderat risiko. Overskotslikviditeten er plassert i bank, styret meiner plasseringa inneber låg risiko for selskapet.</p>
					</section>

					<section>
						<h3>Arbeidsmiljø og personale</h3>

						<p>Ved utgangen av 2014 var det 18,5 årsverk i SIM. Fem av dei tilsette var kvinner. Sjukefråværet var på 6,6 %. Det vart ikkje registrert yrkesskadar eller yrkessjukdom som førte til fråvær, og styra er ikkje kjent med at noko av fråværet skuldast arbeidstilhøve.</p>

						<p>Selskapet har som mål at det skal vera likestilling mellom menn og kvinner på arbeidsplassen. Renovasjonsbransjen har tradisjonelt vore mannsdominert. I eit forsøk på å motverka dette oppmodar selskapet kvinner om å søkja ved utlysing av nye stillingar.</p>

						<p>Bygningar, anlegg og uteområde som selskapet eig er forsøkt utforma i tråd med krava om universell utforming i plan- og bygningslova. Selskapet sin nettstad og informasjonsblad er forsøkt utforma med tanke på blinde og svaksynte lesarar. Selskapet arbeider i personalsamanheng aktivt for å motverka diskriminering, særhandsaming, trakassering og forskjellshandsaming i strid med lov om likestelling mellom kjønna.</p>
					</section>

					<section>
						<h3>Ytre miljø</h3>
						<p>Sigevatnet for Svartasmoget avfallsdeponi hadde i 2014 lik surhetsgrad som dei siste tre åra. Dette viser at nedbrytingsprosessane i avfallet går føre seg slik dei skal, og at metall framleis vert bunde opp i massane.</p>

						<p>Den nedadgåande trenden for nitrogennivået i sigevatnet dei siste åra held fram i 2014, der det gjennomsnittlege nivået for nitrogen i 2014 var det lågaste dei siste fire årene.</p>

						<p>Sigevatnet hadde i 2014 eit jern-nivå som var på linje med tidlegare år. Metallnivået i sigevatnet har variert ein god del igjennom dei siste fire åra, men generelt er jernnivået moderat i sigevatnet frå Svartasmoget. Tungmetallnivået er generelt lågt for dei fleste metall. Tungmetall vil generelt vise seg i låge konsentrasjoner i sigevatn fra kommunale deponi sidan dei fleste slike metall lett blir bundne i avfallsmassane i form av tungt løselige sulfid. Som tidligere år vart det påvist eit svært lågt nivå, eller ikkje kadmium og kvikksølv.</p>
					</section>

					<section>
						<h3>Konsern</h3>

						<p>Dotterselskapa i SIM-konsernet vart selde i september 2014. SIM Næring AS og SIM Tankrenovasjon AS. SIM Næring AS var 100 % eigd av SIM og hadde transport og drift av avfallsordningar for næringslivet som hovudaktivitet. SIM Tankrenovasjon AS var 100 % eigd av SIM Næring AS og tilbydde suge- og spyletenester, røyrinspeksjon, røyrfornying, slamtømming og innsamling av farleg avfall.</p>
					</section>

